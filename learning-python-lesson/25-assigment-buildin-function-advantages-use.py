#     #Built-in Functions in Python


# 1. Definition

# A built-in function is a function that comes pre-defined in Python
# You don’t need to import any library to use it
# Python provides a large number of built-in functions to perform common tasks quickly
# Examples: print(), len(), type(), int(), str(), max(), min(), sum(), range(), input() etc

#******************************


# 2. Use Case of Built-in Functions

# Built-in functions are used to:
# Perform basic operations without writing extra code.

# Example: len("Python") → 6

# Convert data types:
# Example: int("123") → 123

# Perform mathematical operations:
# Example: sum([1,2,3]) → 6

# Input/output operations:
# Example: input("Enter your name: ")

#********************************


# 3. Advantages of Built-in Functions

# Advantage	
# Easy to use	
# Time-saving	
# Reliable	
# Readable code	
# Cross-platform	

#********************


# 4. Where Do We Use Built-in Functions

# Data processing: sum(), len(), max(), min()
# Type conversion: int(), float(), str(), bool()
# Input/output: input(), print()
# Mathematical calculations: round()
# Working with collections: list(), tuple(), set(), dict()
# String operations: len(), chr(), sorted()